    private void cart_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cart_btnActionPerformed
        if(username_btn.getText().equals(null)||username_btn.getText().equalsIgnoreCase("guest")){
            JOptionPane.showMessageDialog(null,"error","PLEASE LOGIN TO PROCEED",JOptionPane.ERROR_MESSAGE);
        }
        else{
            if(Integer.parseInt(cart_count_lbl.getText())==0&&username_btn.getText().equalsIgnoreCase("guest")==false){
                JOptionPane.showMessageDialog(null,"sorry!! your shopping cart is empty","ERROR",JOptionPane.ERROR_MESSAGE);
            }
            else{
                i=0;
                pb1.setVisible(true);
                t=new Timer(8,new ActionListener(){
                    public void actionPerformed(ActionEvent e){
                        pb1.setValue(i);
                        i++;
                        if(i>100){
                            t.stop();
                            new mycart(name1).setVisible(true);
                            dispose();
                        }
                    }
                });
                t.start();
            }
        }
    }//GEN-LAST:event_cart_btnActionPerformed

